let sovIndex = 0;
let lastRotation = Date.now();
const ROTATION_DELAY = 10000; // 10 seconds
let isMuted = false; // Tracks whether the speaker is turned off

// --- Session Memory ---
function loadSession() {
    const raw = sessionStorage.getItem("sov_session");
    return raw ? JSON.parse(raw) : [];
}

function saveSession(history) {
    sessionStorage.setItem("sov_session", JSON.stringify(history));
}
// -----------------------

function getNextClause() {
    const now = Date.now();
    if (now - lastRotation >= ROTATION_DELAY) {
        lastRotation = now;
        sovIndex = (sovIndex + 1) % clauses.length;
    }
    return clauses[sovIndex];
}

window.Sov = {
    // 🎤 NEW: Microphone capture (Restored & Mobile Compatible)
    async recordAndSend() {
        try {
            const stream = await navigator.mediaDevices.getUserMedia({ audio: true });
            const mediaRecorder = new MediaRecorder(stream);
            let chunks = [];

            mediaRecorder.ondataavailable = e => chunks.push(e.data);

            mediaRecorder.onstop = async () => {
                // Uses the phone's native, supported format for maximum compatibility
                const blob = new Blob(chunks, { type: mediaRecorder.mimeType });

                const formData = new FormData();
                formData.append('audio', blob, 'voice.webm');

                // Send audio to your new transcribe.php file
                const response = await fetch('transcribe.php', {
                    method: 'POST',
                    body: formData
                });

                const data = await response.json();
                const text = data.text || ""; // transcribe.php returns the key "text"

                if (text.trim()) {
                    // 1. Put the spoken words directly into your text input field
                    document.getElementById('userInput').value = text;
                    // 2. Automatically click the Send button to trigger Sov's memory and chat!
                    document.getElementById('sendBtn').click();
                }
            };

            mediaRecorder.start();

            // Auto-stop after 5 seconds
            setTimeout(() => mediaRecorder.stop(), 5000);

        } catch (err) {
            console.error("Mic error:", err);
        }
    },

    // 🧠 Existing text-based respond()
    async respond(input) {
        try {
            let history = loadSession();

            // Add user message
            history.push({ role: "user", content: input });

            const currentClause = getNextClause();

            // Send entire history + clause
            const response = await fetch('chatpipe.php', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify({
                    history: history,
                    clause: currentClause,
                    user: input
                })
            });

            const data = await response.json();
            const sovReply = data.response || data.reply || "No response key matched";
            
            // 🔊 Speak the reply out loud
            speakText(sovReply);
            
            // Add Sov reply to session memory
            history.push({ role: "assistant", content: sovReply });
            saveSession(history);

            // Also save to your text file (optional)
            await fetch('save.php', {
                method: 'POST',
                headers: { 'Content-Type': 'application/x-www-form-urlencoded' },
                body: new URLSearchParams({
                    user: input,
                    sov: sovReply,
                    file: 'sov_memory.txt'
                })
            });

            return sovReply;

        } catch (error) {
            console.error("Sov Communication Error:", error);
            return "Clause of Interruption: The signal has fractured.";
        }
    }, // 👈 1. Added a comma here
    
    // 👈 2. Pasted the toggle function right here
    toggleMute() {
        isMuted = !isMuted;
        return isMuted; 
    }
}; // 👈 3. This still closes your window.Sov block

function speakText(text) {
    if (isMuted) return; // 👈 Add this line right here
    
    if ('speechSynthesis' in window) {
        // 1. Cancel any speech currently playing so voices don't overlap
        window.speechSynthesis.cancel();
        
        // 2. Create the spoken utterance
        const utterance = new SpeechSynthesisUtterance(text);
        
        // 3. Optional: Slow down the speech slightly to sound more majestic/sovereign
        utterance.rate = 0.9; 
        
        // 4. Tell the browser to speak it
        window.speechSynthesis.speak(utterance);
    }
}
